package main

import "fmt"

func main() {
	numero := 10
	numero++ //numero = numero + 1
	fmt.Println(numero)

	numero-- //numero = numero - 1
	fmt.Println(numero)

	numero += 20 //numero = numero + 20
	fmt.Println(numero)

	numero -= 10 //numero = numero - 10
	fmt.Println(numero)

	numero *= 3 //numero = numero * 3
	fmt.Println(numero)
}
